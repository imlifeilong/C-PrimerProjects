// 引入vector头文件：提供动态数组（vector）的定义和相关操作，用于存储整数序列
#include <vector>
// 引入iostream头文件：提供输入输出流功能，用于打印数组内容
#include <iostream>
// 使用std命名空间：避免每次使用标准库组件时都要写std::前缀，简化代码书写
using namespace std;

// 定义解决方案类，包含两种移动零的实现方法（暴力解法+双指针最优解法）
class Solution {
public:
    /**
     * 暴力解法：通过元素前移+末尾补零实现零元素移动（直接修改原vector，无返回值）
     * @param nums 引用传递的整数动态数组，需将所有零移动到末尾且保持非零元素相对顺序
     * 注：引用传递(&)是为了直接修改原始数组，避免不必要的数组拷贝，提升效率
     */
    void moveZeroes_brute(vector<int>& nums) {
        // 获取当前数组的有效长度（后续会逐步缩小遍历范围）
        int n = nums.size();

        // 外层循环：遍历数组的每个位置，用于检测零元素
        for (int i = 0; i < n; i++) {
            // 当当前遍历到的元素是零时，需要进行移动处理
            if (nums[i] == 0) {
                // 内层循环：将当前零元素后面的所有元素向前移动一位，覆盖当前零的位置
                for (int j = i + 1; j < n; j++) {
                    nums[j - 1] = nums[j];
                }
                // 经过元素前移后，原有效长度的末尾位置变为空闲，补零完成当前零元素的移动
                nums[n - 1] = 0;
                // 关键：元素前移后，当前i位置会被原i+1位置的元素填充，该元素可能也是零
                // 因此i需要减1，在下一次循环中重新检查当前位置的新元素
                i--;
                // 关键：末尾已经固定了一个零元素，后续无需再遍历该位置，因此有效长度n减1
                n--;
            }
        }
    }

    /**
     * 双指针最优解法：通过交换将非零元素前置，零元素自然后置（直接修改原vector，无返回值）
     * @param nums 引用传递的整数动态数组，需将所有零移动到末尾且保持非零元素相对顺序
     * 时间复杂度O(n)，空间复杂度O(1)，是该问题的最优解法
     */
    void moveZeroes(vector<int>& nums) {
        // 获取数组总长度，固定不变（与暴力解法的动态n不同）
        int n = nums.size();
        // 左指针（慢指针）：标记下一个非零元素应该放置的目标位置，初始化为0
        int left = 0;
        // 右指针（快指针）：用于遍历整个数组，寻找非零元素，初始化为0
        int right = 0;

        // 循环：右指针遍历数组，直到遍历完所有元素
        while (right < n) {
            // 当右指针找到非零元素时，需要将其放到左指针标记的目标位置
            if (nums[right] != 0) {
                // 交换左右指针指向的元素：非零元素移到左指针位置，零元素移到右指针位置
                swap(nums[left], nums[right]);
                // 左指针右移一位，准备接收下一个非零元素
                left++;  // 左指针右移
            }
            // 无论当前元素是否为零，右指针始终右移，继续遍历下一个元素
            right++;  // 右指针始终右移
        }
    }
};

// 主函数：程序入口，用于测试移动零的双指针最优解法
int main() {
    // 创建Solution类的实例，用于调用其成员函数
    Solution solution;
    // 定义测试用的动态数组：包含零元素和非零元素的示例序列
    vector<int> nums = { 0, 1, 0, 3, 12 };

    // 打印原数组，用于与处理后的数组对比
    cout << "原数组: ";
    // 范围for循环：遍历nums中的每个元素并打印（C++11及以上支持）
    for (int num : nums) {
        cout << num << " ";
    }
    // 换行，优化输出格式
    cout << endl;

    // 调用双指针最优解法处理数组（直接修改nums的原始内容）
    solution.moveZeroes_brute(nums);
    //solution.moveZeroes(nums);

    // 打印处理后的数组，查看零元素移动结果
    cout << "移动零后: ";
    // 范围for循环：遍历处理后的nums并打印
    for (int num : nums) {
        cout << num << " ";
    }
    // 换行，优化输出格式
    cout << endl;

    // 程序正常结束，返回0
    return 0;
}